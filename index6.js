let fun = (arr) => {
  let arr1 = [];
  for (let i = 0; i < arr.length; i++) {
    switch (arr[i]) {
      case "0":
        arr1.push(0);
        break;
      case "1":
        arr1.push(1);
        break;
      case "2":
        arr1.push(2);
        break;
      case "3":
        arr1.push(3);
        break;
      case "4":
        arr1.push(4);
        break;
      case "5":
        arr1.push(5);
        break;
      case "6":
        arr1.push(6);
        break;
      case "7":
        arr1.push(7);
        break;
      case "8":
        arr1.push(8);
        break;
      case "9":
        arr1.push(9);
        break;
      case "C":
        {
          let a = arr.indexOf(arr[i]);
          arr1.splice(a - 1, 1);
        }

        break;
      case "D": {
        let b = arr.indexOf(arr[i]);

        if (arr1.length < 2) {
          arr1.push(2 * arr1[0]);
        } else {
          arr1.push(2 * arr1[b - 2]);
        }

        break;
      }
      case "+": {
       
        let a = arr1[0] + arr1[1];

        arr1.push(a);
        break;
      }
    }
  }
  return arr1.reduce((a, b) => {
    return a + b;
  }, 0);
};
